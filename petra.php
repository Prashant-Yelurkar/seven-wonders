          <h3>Information</h3>
          <P>
            Famous for its rock-cut architecture and water conduit system, Petra is also called the "Red Rose City" because of the colour of the stone from which it is carved. It has been a UNESCO World Heritage Site since 1985. UNESCO has described Petra as "one of the most precious cultural properties of man's cultural heritage". In 2007, Al-Khazneh was voted one of the New 7 Wonders of the World. Petra is a symbol of Jordan, as well as Jordan's most-visited tourist attraction.
          </P>
          <hr>
          <h3>History</h3>
          <p>
            Petra is believed to have been settled as early as 9,000 BC. It became the capital city of the Nabataean Kingdom. The Nabataeans were nomadic Arabs who used Petra because it was near the spice trade routes.
            The Nabataean Kingdom became a client state of the Roman Empire in the first century BC. In 106 AD they lost their independence. Petra's importance declined as sea trade routes developed. Also, the 363 earthquake destroyed many structures.
          </p>
          <hr>
          <h3>Construction</h3>
          <p>
            As the Treasury sits between the canyons' gorges, the Nabateans had to come up with a way to control flash floods and sudden water surges that may potentially ruin the carved structure. They did this by constructing a series of dams made from stone blocks mortared together and anchored to carved out canyon groves.
          </p>
          <hr>
