          <h3>History</h3>
          <p>
            The First Emperor of China, Qin Shi Huang also called Shi Huangdi, started the Qin Dynasty. The Xiongnu tribes in the north of China were his enemies. The land in some parts of China is easy to cross, so Qin Shi Huang started building the Great Wall to make it more difficult for the Xiongnu to invade China.<br>
            The Great Wall of China is a series of fortifications that were built across the historical northern borders of ancient Chinese states. It is the longest structure humans have ever built. It is about 21,196 kilometers long, 9.1 metres (30 feet) wide and 15 metres high. It is made over the course of hundreds of years, the wall was built by over 6 different Chinese dynasties, and is over 2,300 years old.<br>
            The Great Wall was designated a UNESCO World Heritage site in 1987. The Great Wall was declared as one of the Seven Wonders of the World in 2007.<br>
          </p>
          <hr>
          <h3>Construction</h3>
          <p>
            Builders used materials that were nearby. Some parts of the wall were made of mud, straw, and twigs. Thousands of workers died from giant falling stones, exhaustion, disease, animal attacks, and starvation. Workers dying and being buried in and under the Great Wall is a myth.
          </p>
          <hr>
          <h3>Visibility from space</h3>
          <p>Rumours about astronauts being able to see the Great Wall from the moon are scientifically not proven.The Great Wall has shown up in radar images taken from space, but scientists are sure it is not possible for astronauts to see the wall with a naked eye. One astronaut who spoke about the visibility of the Great Wall from space was Neil Armstrong. He said that on the moon, it was very clear that the wall was not visible. However, astronaut William Pogue was able to see the wall from a Low Earth Orbit distance (300-530 km height), but only with binoculars and with lots of practice</p>
