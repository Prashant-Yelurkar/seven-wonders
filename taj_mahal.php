          <h3>Information</h3>
          <P>
            The Taj Mahal is an Islamic religious building, mosque and tomb built in the 17th century by king Shah Jehan in memory of his wife, Mumtaz Mahal. Its chief architect was Ustad Ahmad Lahauri. The building is in the city of Agra, Uttar Pradesh.It is located on the south bank of Yamuna river in Agra.
          </P>
          <hr>
          <h3>Story</h3>
          <p>
            Often described as one of the wonders of the world, the stunning 17th Century white marble Taj Mahal was built by Mughal emperor Shah Jahan as a mausoleum for his beloved wife Mumtaz Mahal, who died in childbirth. The conventional story behind it is Shah Jahan's eternal love for Mumtaz.
          </p>
          <hr>
          <h3>History</h3>
          <p>
            It was built by Mughal Emperor Shah Jahan in memory of his wife Mumtaz Mahal with construction starting in 1632 AD and completed in 1648 AD, with the mosque, the guest house and the main gateway on the south, the outer courtyard and its cloisters were added subsequently and completed in 1653 AD.
          </p>
          <hr>
          <h3>Construction</h3>
          <p>
            It was designed by Ustad Ahmad Lahauri, and is generally regarded as the best example of Mughal architecture, which is a blend of Indian, Persian and Islamic styles. The architectural principles the Taj Mahal encapsulates include: Rational and strict geometry. Perfect symmetrical planning.
          </p>
